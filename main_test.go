package linear

import (
	"testing"

	"github.com/ready-steady/assert"
)

func TestTensor(t *testing.T) {
	x := []float64{1, 2, 3}
	y := []float64{4, 5, 6}
	z := []float64{7, 8, 9}

	T := []float64{
		1, 4, 7,
		2, 4, 7,
		3, 4, 7,
		1, 5, 7,
		2, 5, 7,
		3, 5, 7,
		1, 6, 7,
		2, 6, 7,
		3, 6, 7,
		1, 4, 8,
		2, 4, 8,
		3, 4, 8,
		1, 5, 8,
		2, 5, 8,
		3, 5, 8,
		1, 6, 8,
		2, 6, 8,
		3, 6, 8,
		1, 4, 9,
		2, 4, 9,
		3, 4, 9,
		1, 5, 9,
		2, 5, 9,
		3, 5, 9,
		1, 6, 9,
		2, 6, 9,
		3, 6, 9,
	}

	assert.Equal(Tensor(x, y, z), T, t)

	x = []float64{1, 2}
	y = []float64{3, 4}
	z = []float64{5, 6}
	w := []float64{7, 8}

	T = []float64{
		1, 3, 5, 7,
		2, 3, 5, 7,
		1, 4, 5, 7,
		2, 4, 5, 7,
		1, 3, 6, 7,
		2, 3, 6, 7,
		1, 4, 6, 7,
		2, 4, 6, 7,
		1, 3, 5, 8,
		2, 3, 5, 8,
		1, 4, 5, 8,
		2, 4, 5, 8,
		1, 3, 6, 8,
		2, 3, 6, 8,
		1, 4, 6, 8,
		2, 4, 6, 8,
	}

	assert.Equal(Tensor(x, y, z, w), T, t)
}
